package com.github.klikli_dev.theurgy.common.block;


import com.github.klikli_dev.theurgy.common.theurgy.IAetherInformationProvider;
import com.github.klikli_dev.theurgy.common.tile.AetherGeneratorTile;
import com.github.klikli_dev.theurgy.common.tile.CreativeAetherSourceTile;
import net.minecraft.block.Block;
import net.minecraft.block.BlockState;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.world.IBlockReader;

import javax.annotation.Nullable;

public class AetherGeneratorBlock extends Block implements IAetherInformationProvider {
    //region Initialization
    public AetherGeneratorBlock(Properties properties) {
        super(properties);
    }
    //endregion Initialization

    //region Overrides
    @Override
    public boolean hasTileEntity(BlockState state) {
        return true;
    }

    @Nullable
    @Override
    public TileEntity createTileEntity(BlockState state, IBlockReader world) {
        return new AetherGeneratorTile();
    }
    //endregion Overrides
}
